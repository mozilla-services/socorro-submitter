# List installed requirements, drop pip and rapid-client, drop first
# two lines, and convert intermediary spaces in each line to ==
echo "# Auto-generated by bin/list_runtime_reqs.sh at $(date)"
pip list | \
    tail -n +3 | \
    sed -r 's/( )+/==/g' | \
    grep -v "pip==" | \
    grep -v "rapid-client=="
